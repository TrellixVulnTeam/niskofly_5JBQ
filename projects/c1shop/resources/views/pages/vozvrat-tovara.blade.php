            <div class="title_bold">I. Возврат/обмен товара надлежащего качества Покупателем</div>

            <p>
                Товар надлежащего качества обмену и возврату не подлежит, если иное не предусмотрено договором между
                покупателем и поставщиком, а также в случаях:
            </p>

            <ul>
                <li>товар не соответствует ассортименту (п.3 ст.468 ГК РФ);</li>
                <li>товар не соответствует качеству (п.2 ст.475 ГК РФ);</li>
                <li>товар не соответствует комплекту (п.2 ст.480 ГК РФ);</li>
                <li>товар не соответствует установленной таре (ст. 482 ГК РФ)</li>
            </ul>

            <p>
                Кроме того, не подлежит возврату товар:
            </p>

            <ul>
                <li>изготовленный по индивидуальным размерам, цвету, фасону и т.п. покупателя (не стандартный товар);
                </li>
                <li>стандартный товар, но с индивидуальными вырезами, разрезами и т.п., что нарушает целостность
                    товара;
                </li>
                <li>товар, не являющийся складской позицией.</li>
            </ul>

            <div class="title_bold">Порядок действий при возврате нового товара</div>

