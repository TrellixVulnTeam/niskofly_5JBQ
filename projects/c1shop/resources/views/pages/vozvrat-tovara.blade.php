            <div class="title_bold">II. Возврат/обмен товара надлежащего качества Потребителем</div>
            <p>
                Потребитель вправе обменять товар надлежащего качества, если указанные товар не подошел по форме, габаритам,
                фасону, расцветке, размеру или комплектации, в течение 14 (четырнадцати) дней, не считая дня покупки. Обмен
                товара надлежащего качества проводится, если указанный товар не был в употреблении, сохранены его товарный
                вид, потребительские свойства, пломбы, фабричные ярлыки, а также имеется товарный/кассовый чек, либо иной
                подтверждающий оплату указанного товара документ (п. 1 ст. 25 Закона «О защите прав потребителей», ст. 502
                ГК РФ)
            </p>

            <p>
                В случае, если аналогичный товар отсутствует в продаже на день обращения потребителя к продавцу, потребитель
