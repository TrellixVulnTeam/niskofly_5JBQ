                денежной суммы. Требование потребителя о возврате уплаченной за указанный товар денежной суммы подлежит
                удовлетворению в течение 3 (трех) дней со дня возврата указанного товара. По соглашению потребителя с
                продавцом обмен товара может быть предусмотрен при поступлении аналогичного товара в продажу. Продавец
                обязан незамедлительно сообщить потребителю о поступлении аналогичного товара в продажу. (п. 2 ст. 25 Закона
                «О защите прав потребителей»).
            </p>

            <p>
                При дистанционном способе продажи товаров (п. 4 ст. 497 ГК РФ) Потребитель вправе отказаться от товара в
