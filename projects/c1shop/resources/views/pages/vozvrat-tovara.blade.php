                    производиться монтаж.
                </li>
                <li>Фотографии для рекламаций по пересортице в обязательном порядке сопровождаются фотографиями шильд,
                    где просматриваются парт-номера, данные по товару, а также упаковки изделия.
                </li>
                <li>Фотографии для рекламаций по качеству работы, а также недокомплектации сложно-технического
                    оборудования в обязательном порядке должны сопровождаться фотографиями шильд, где просматриваются
                    парт-номера, данные по товару.
                </li>
                <li>Фотографии механических дефектов (боя) в обязательном порядке должны сопровождаться фотографиями
                    упаковки.
                </li>
            </ol>
