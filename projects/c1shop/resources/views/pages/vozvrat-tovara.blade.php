                    предусмотрено договором между покупателем/потребителем и поставщиком/продавцом.
                </li>
            </ol>

            <div class="title_bold">Порядок составления рекламации</div>

            <p>
                Рекламация должна содержать следующие данные:
            </p>

            <ol>
                <li>№ товарной накладной и дата товарной накладной, в соответствии с которой данный товар был
                    отгружен.
                </li>
                <li>Артикул, точное наименование неотгруженного (излишне отгруженного, дефектного, некомплектного)
                    товара.
                </li>
                <li>Заводской номер изделия, номер партии, дата производства, если данная информация присутствует на
                    оборудовании, либо упаковке изделия.
                </li>
                <li>Описание (перечень) заявленных дефектов (при их наличии).</li>
                <li>Количество, согласно товарной накладной (заполняется в прилагаемом Акте разногласий).</li>
                <li>Фактическое количество (при недогрузе) (заполняется в прилагаемом Акте разногласий).</li>
                <li>Отсутствующие элементы комплектации (при недокомплекте).</li>
                <li>Количество излишков (при излишней отгрузке) (заполняется в прилагаемом Акте разногласий).</li>
                <li>Требования заявителя по урегулированию рекламации (замена, возврат, ремонт и т.д.).</li>
                <li>Причина возврата.</li>
                <li>Приложения: фотографии (обязательны для рекламаций по дефектам, пересортице, бою, излишкам (при
                    невозможности их идентифицировать).
                </li>
                <li>Подпись и печать (при ее наличии) заявителя.</li>
            </ol>

            <p>
                Рекомендации по проведению фотографирования рекламационного объекта:
            </p>

            <ol>
