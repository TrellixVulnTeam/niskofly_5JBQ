            <div class="title_bold">III. Возврат/обмен товара ненадлежащего качества покупателями/потребителями</div>

            <p>
                Под товаром ненадлежащего качества понимается товар, который неисправен и не может обеспечить исполнение
                своих функциональных качеств. Отличие элементов дизайна или оформления от заявленных в описании не является
                неисправностью или нефункциональностью товара.
            </p>

            <p>
                При выборке товара на складе поставщика/продавца (самовывоз) внешний вид и комплектность товара проверяются
                покупателем в момент получения товара, после получения товара претензии к внешним дефектам товара, его
                количеству, комплектности и товарному виду не принимаются, если иное не предусмотрено договором между
                покупателем и поставщиком/продавцом (ст. 513 ГК РФ). В остальных случаях внешний вид и комплектность товара
                проверяются покупателем в течение 10 (десяти) дней с момента получения товара.
            </p>

            <p>
                В соответствии со ст. ст.475 (503, 518) Гражданского кодекса РФ покупатель/потребитель вправе:
            </p>
            <ul>
                <li>в случае, если недостатки товара не были оговорены поставщиком/продавцом потребовать от продавца
                    безвозмездного устранения недостатков товара в разумный срок (ремонт) или возмещения своих расходов
                    на устранение недостатков товара;
                </li>
                <li>в случае существенного нарушения требований к качеству товара отказаться от исполнения договора
                    купли-продажи и потребовать возврата уплаченной за товар денежной суммы или потребовать замены
                    товара ненадлежащего качества товаром, соответствующим договору.
                </li>
            </ul>

            <p>
                Покупатель/потребитель вправе обменять, возвратить товар ненадлежащего качества или потребовать возврата
